export type Table = [string[], ...number[][]]

const table = TABLE
  // get every row
  .split("\n")
  // find rows that aren't seperators or empty
  .filter(l => l.charAt(0) !== "+" && l !== "")
  // map each row
  .map(val =>
    val
      // get each value in the row
      .split("|")

      // remove the padding around the value
      .map(v => v.trim())

      // remove empty values
      .filter(v => v !== "")

      // change ½ to 3 (for 1/2)
      .map(v => (v === "½" ? "3" : v))

      // make the strings numbers, but not the header
      .map(v => (Number.isInteger(v) ? parseInt(v, 10) : v))
  ) as Table;

